    if depth == 1:
        if n in range(0, 2):
            return [1]
        elif n in range(2, 4):
            return [1, 2]
        if n in range(4, 9):
            return [1, 3, 4]
        elif n in range(9, 13):
            return [1, 3, 5, 6]
        elif n in range(13, 17):
            return [1, 3, 5, 7, 8]
        elif n in range(17, 21):
            return [1, 2, 5, 8, 9, 10]
        elif n in range(21,27):
            return [1, 2, 5, 8, 11, 12, 13]
        else:
            raise RuntimeError("Max power out of range.")
    elif depth == 2:
        if n in range(0, 5):
            return [1]
        elif n in range(5, 9):
            return [1, 2]
        elif n in range(9, 27):
            return [1, 5, 8]
        elif n in range(27, 45):
            return [1, 3, 11, 18]
        elif n in range(45, 71):
            return [1, 3, 11, 15, 32]
        else:
            raise RuntimeError("Max power out of range.")
    elif depth == 3:
        if n in range(0, 9):
            return [1, 3, 4]
        elif n in range(9, 27):
            return [1, 5, 8]
        elif n in range(27, 53):
            return [1, 7, 12]
        elif n in range(53, 72):
            return [1, 4, 12, 21]
        elif n in range(72, 115):
            return [1, 4, 19, 33]
        elif n in range(115,217):
            return [1, 7, 12, 43, 52]
        else:
            raise RuntimeError("Max power out of range.")
    else:
        raise RuntimeError("HE depth out of range.")



